class Solution {
  public:
    int findTime(string S1, string S2) {
        // code here
        int curr=0;
       
        int temp=0,sum=0;
        for(int j=0; j<S2.size(); j++){
            curr=S1.find(S2[j]);
            sum+=abs(curr-temp);
            temp=curr;
        }
        return sum;
        
    }
};

